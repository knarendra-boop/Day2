package org.example.gneraricspractice;

public class MultipleValues <k,v>{
    k key;
    v value;

    public MultipleValues() {
        this.key = key;
        this.value = value;
    }

    public k getKey() {
        return key;
    }
    public void setKey(k key) {
        this.key = key;
    }
    public v getValue() {
        return value;
    }

    public void setValue(v value) {
        this.value = value;
    }
}

